void solve(){
    string s;
    cin >> s;
    stack<pair<char,int>> small,cap;
    for(int i = 0; i < s.length(); i++){
        if(s[i] == 'b'){
            if(small.empty() == false){
                small.pop();
            }
        }
        else if(s[i] == 'B'){
            if(cap.empty() == false){
                cap.pop();
            }
        }
        else if(s[i] >= 'a' and s[i] <= 'z'){
            small.push(make_pair(s[i],i));
        }
        else{
            cap.push(make_pair(s[i],i));
        }
    }

    vtr<pair<char,int>> v;
    while(small.empty() == false){
        v.push_back(small.top());
        small.pop();
    }
    while(cap.empty() == false){
        v.push_back(cap.top());
        cap.pop();
    }
    sort(v.begin(),v.end(),[](auto a,auto b)
    {return a.second < b.second;});

    string ans = "";
    for(auto val: v){
        ans += val.first;
    }
    cout << ans << el;
}
