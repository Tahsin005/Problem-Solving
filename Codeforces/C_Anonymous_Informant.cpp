void solve(){
    ll n,k;
    cin>>n>>k;
    vll v(n);
    for(int i = 0; i < n; i++) cin>>v[i];

    ll y = n - 1;
    ll flag = true;
    set<ll> st;
    for(int i = 0; i < k; i++){
        if(st.count(y)) break;
        if(v[y] > n)
        {
            flag = false;
            break;
        }
        st.insert(y);
        y = (y - v[y] + n) % n;
    }
    if(flag) cout<<"Yes"<<el;
    else cout<<"No"<<el;
}
