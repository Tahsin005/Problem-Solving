void solve(){
    ll n, b, c, d;
    cin >> n >> b >> c >> d;
    ll temp = (n - n % 7) / 7 + 1;
    temp -= (n % 7 == 0 ? 1 : 0);
    ll l = 1, r = temp;
    ll ans = temp;
    while(l <= r){
        ll mid = l + (r - l) / 2;
        if(b <= (mid + 1) / 2 * c + d * mid){
            ans = mid;
            r = mid - 1;
        }
        else{
            l = mid + 1;
        }
    }
    ll temp2 = (ans + 1) / 2;
    ll howMuch = temp2 * c + d * ans;
    b = b - howMuch;
    ll tmp = temp2;
    b = max(b, 0LL);
    tmp = tmp + (b / c);
    tmp = tmp + (b % c != 0);
    cout << n - tmp << " " << el;
}
