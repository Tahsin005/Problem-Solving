void solve(){
    int n;
    cin>>n;
    int h2,h1,h3;

    for(int i = 1; i <= n; i++){
        h1 = i;

        int rem = n - h1;

        if(rem & 1){
            //Odd
            h2 = (rem + 1) / 2;
        }
        else{
            //Even
            h2 = (rem / 2) + 1;
        }

        h3 = n - h1 - h2;

        if(h1 > 0 and h2 > 0 and h3 > 0 and h2 < h1 and h3 < h2){
            break;
        }
    }

    cout<<h2<<" "<<h1<<" "<<h3<<el;
}
