void solve(){
    int n;
    cin>>n;
    int a[n];
    for(int i = 0; i < n; i++) cin >> a[i];
    sort(a,a + n);
    int l = -1, r = 1e9 + 1;
    while(r - l > 1){
        int m = (l + r) / 2;
        int c = 1;
        int val = 0;
        for(int i = 1; i < n; i++){
            if(a[i] - a[val] > m * 2){
                c++;
                val = i;
            }
        }
        if(c <= 3){
            r = m;
        }
        else{
            l = m;
        }
    }
    cout<<r<<el;
}
