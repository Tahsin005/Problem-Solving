bool isPrime(int n) {
    // Handling base cases
    if (n <= 1) {
        return false;
    }

    // Check for divisibility up to the square root of n
    for (int i = 2; i <= sqrt(n); ++i) {
        if (n % i == 0) {
            // If n is divisible by any number, it's not prime
            return false;
        }
    }

    // If no divisor found, n is prime
    return true;
}
void solve(){
    int h;
    cin >> h;
    int cnt = 0;
    int cp = 1;
    while(h > 0){
        if(isPrime(h)){
            h = 0;
            cnt++;
            break;
        }
        else{
            h -= cp;
            cp *= 2;
            cnt++;
        }
    }
    if(h != 0){
        cout << -1 << el;
    }
    else{
        cout << cnt << el;
    }
}
