void solve(){
    ll n;
    cin >> n;
    vii v(n + 1);
    for(ll i = 1; i <= n; i++) cin >> v[i];

    ll ans = 0;
    ll idx = 1;
    sort(all(v));
    vii arr(n + 1,0);
    for(ll i = 2; i <= n; i += 2){
        arr[i] = v[idx];
        idx++;
    }

    for(ll i = 1; i <= n; i++){
        if(arr[i] == 0){
            arr[i] = v[idx];
            idx++;
        }
    }

    for(ll i = 2; i < n; i += 2){
        if(arr[i] < arr[i - 1] and arr[i] < arr[i + 1]){
            ans++;
        }
    }
    cout << ans << el;
    for(ll i = 1; i <= n; i++){
        cout << arr[i] << " ";
    }
}
